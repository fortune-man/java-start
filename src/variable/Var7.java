package variable;

public class Var7 {

  public static void main(String[] args) {
    int a = 100; // 정수
    double b = 10.5; // 실수
    boolean c = true; // 불리언 boolean
    char d = 'A'; // 문자 하나
    String e = "Hello world"; // 문자열을 다루기 위한 데이터 타입
    // 코드에서 개발자가 직접 적은 100, 10.5, true, 'A', "Hello Java"와 같은 값을 리터럴(literal)이라고 부름
    // 개발자가 직접 입력한 고정된 값. 리터럴
    System.out.println(a);
    System.out.println(b);
    System.out.println(c);
    System.out.println(d);
    System.out.println(e);
  }



}
